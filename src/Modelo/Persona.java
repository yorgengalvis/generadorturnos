package Modelo;

/**
 *
 * @author YorgenGalvis
 */
public class Persona {
    String name;
    String dni;
    String telefono;

    public Persona(String name, String dni, String telefono) {
        this.name = name;
        this.dni = dni;
        this.telefono = telefono;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    
    
}
